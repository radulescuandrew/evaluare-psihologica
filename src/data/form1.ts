const mapSentence = (prompts: string[]): QuestionDef[] =>
  prompts.map((prompt, idx) => ({
    id: `f1_tav_${idx + 1}`,
    formId: "form1",
    sectionId: "tav",
    prompt,
    type: "sentence_completion",
    required: true,
  }));

const mapLikert = (prompts: string[], sectionId: string, formId: "form1", options: ScaleOption[]): QuestionDef[] =>
  prompts.map((prompt, idx) => ({
    id: `${formId}_${sectionId}_${idx + 1}`,
    formId,
    sectionId,
    prompt,
    type: "likert_uniform",
    required: true,
    options,
  }));

